func IsCommandFile(buffer *[]byte) bool {
	match := "^([ ]*)file([ ]+)(([a-zA-Z0-9]*)+)[.]txt$"
	is_match, error := regexp.Match(match, *buffer)
	if error != nil {
		return false
	}
	return is_match
}

